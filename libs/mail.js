export const sender = nodemailer.createTransport({
  service: "gmail",
  auth: {
    user: process.env.MAIL_USERNAME,
    pass: process.env.MAIL_PASSWORD,
  },
});

function mailSender(mailOptions) {
  sender.sendMail(mailOptions, (error, info) => {});
}

export async function sendVerificationEmail(email, token) {
  const confirmLink = `${process.env.APP_DOMAIN}/verification?token=${token}`;

  const mailOptions = {
    from: process.env.MAIL_USERNAME,
    to: email,
    subject: "Verify Your Email",
    html: VERIFY_EMAIL_TEMPLATE.replace("{confirmLink}", confirmLink),
  };
  mailSender(mailOptions);
}

export async function sendPasswordResetEmail(email, token) {
  const resetLink = `${process.env.APP_DOMAIN}/reset-password?token=${token}`;
  const mailOptions = {
    from: process.env.MAIL_USERNAME,
    to: email,
    subject: "Reset your password",
    html: RESET_PASSWORD_EMAIL_TEMPLATE.replace("{resetLink}", resetLink),
  };
  mailSender(mailOptions);
}
